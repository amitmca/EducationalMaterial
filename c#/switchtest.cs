
	class switchtest
	{
	public static void Main(string[] args)
		{
		int i=5;
		switch(i)
			{
			case 1:
				System.Console.WriteLine("One");
				break;
			case 2:
				System.Console.WriteLine("Two");
				break;
			default:
				System.Console.WriteLine("Default");

			}

		}
	}	

